
public class zerohundred {

	public static void main(String[] args) {
		int number = 11;
		if (number >= 0 && number <= 10) System.out.println("0-10");
		if (number > 10 && number <= 20) System.out.println("11-20");
		if (number > 21 && number <= 30) System.out.println("21-30");
		if (number > 31 && number <= 40) System.out.println("31-40");
		if (number > 41 && number <= 50) System.out.println("41-50");
		if (number > 51 && number <= 60) System.out.println("51-60");
		if (number > 61 && number <= 70) System.out.println("61-70");
		if (number > 71 && number <= 80) System.out.println("71-80");
		if (number > 81 && number <= 90) System.out.println("81-90");
		if (number > 91 && number <= 100) System.out.println("91-100");
		//else System.out.println("Invalid entry");
		}
}
